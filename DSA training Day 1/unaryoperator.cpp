
#include <iostream>
using namespace std;

int main()
{
    int a = 10;
    cout<< a++ << endl;
    cout<< a << endl;

    
    int b = 10;
    cout<< ++b << endl;
    cout<< b << endl;


    int c = 10;
    cout<< c-- << endl;
    cout<< c << endl;   


    int d = 10;
    cout<< --d << endl;         
    cout<< d << endl;
    return 0;
}